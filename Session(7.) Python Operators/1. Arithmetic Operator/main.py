# Arithmetic Operators

# ___Addition Operator___
x_add = 10
y_add = 51
print(x_add + y_add)

# ___Subtraction Operator___
x_sub = 12
y_sub = 3
print(x_sub - y_sub)

# ___Multiplication Operator___
x_mul = 4
y_mul = 6
print(x_mul * y_mul)

# ___Division Operator___
x_div = 16
y_div = 5
print(x_div / y_div)

# ___Floor Division Operator___
x_flo_Div = 16
y_flo_Div = 5
print(x_flo_Div//y_flo_Div) #yeh decimal k baad wala number nhi krta display

# Modulus Operator
x_mod = 16
