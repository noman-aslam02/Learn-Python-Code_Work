# String Concatenation in Python

# Wrong WAY
# ___Merge Two Variables___
# here we used + operator, will already know what is + operator
x = "Hello"
y = "World"
print(x + y)  # this is not good practice

#Right Way
# ___Merge Two Variables___
x1 = "Hello"
sp = " "
y1 = "World"
print(x1 + sp + y1)  # this is good practice
# or
x2 = "Hello "
y2 = "World"
print(x2 + y2)  # this is good practice